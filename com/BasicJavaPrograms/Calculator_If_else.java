package com.BasicJavaPrograms;

public class Calculator_If_else {

	public static void main(String[] args) {
		int x = 20;
		int y = 10;
		char c = 'a';
		if (c == 'm') {
			int multi = x * y;
			System.out.println(multi);
		} else if (c == 'a') {
			int add = x + y;
			System.out.println(add);
		}
	}

}
